            <h2 class="text-center">Museum batik pekalongan</h2>
             <p class="justify">  Museum Batik Pekalongan diresmikan oleh Presiden Republik Indonesia ke-6 Susilo Bambang Yudhoyono pada tanggal 12 Juli 2006. Banhunan Museum ini mempunyai luas sekitar 2500 m2menempati lahan seluas 3675 m2. Bangunan yang ditempati adalah bangunan peninggalan Belanda yang telah berdiri sejak tahun 1906 yang pada awalnya berfungsi sebagai kantor administrasi keuangan pabrik gula yang berada di sekitar area karisidenan Pekalongan. Pada perkembangannya, gedung ini mengalami beberapakali perubahan fungsi sebagai Balai Kota, Kantor Walikota, hingga komplek perkantoran Pemerintah Kota.
Museum Batik Pekalongan menyimpan banyak koleksi batik tua hingga modern baik itu yang berasal dari daerah pesisiran, daerah pedalaman dan area Jawa lainnya, batik dari berbagai daerah di Nusantaara seperti dari Sumatera, Kalimantan, hingga Papua, dan kain jenis teknik batik dari manca negara.
Tidak hanya memamerkan koleksi batik, Museum Batik Pekalongan juga adalah pusat pelatihan membatik dan pusat pembelajaran batik. Pelajar maupun pengunjung umum dapat belajar membuat batik ataupun melakukan penelitian mengenai budaya batik. Museum Batik Pekalongan juga mempunyai program-program pelatihan membatik baik ke masyarakat secara langsung, ke berbagai sekolah, hingga ke berbagai institusi lainya. Berbagai kerjasama dengan berbagai pihak juga dilakukan guna pelestarian budaya batik. Semua hal itu dilakukan sesuai dengan komitmen Museum Batik Pekalongan untuk terus menjaga dan melestarikan budaya warisan nenek moyang yang bernilai adiluhung ini.<p>

 <p>&#10061;Motif Isen<p/>
  <p href="#"class="justify"; </p>Motif batik satu ini menggunakan teknik pengisian berupa isen-isen, atau banyak dikenal dengan titik-titik. Yang di dalam bahasa jawa seringnya disebut cecek-cecek. Titik tersebut bisa berupa titik garis atau titik pitu. Sebenarnya sangat jarang dijumpai jika ingin menemukan batik titik sawut, atau cacah gori. Karena di Pekalongan hanya berupa 2 teknik pengisian itu saja. Terkadang pengisian cecek sangat banyak, hingga semua motif berupa titik. Batik tulis yang terkenal dengan motif ini yaitu karya “OeiTjowSoen”.<br/>
    <p>&#10061;Motif Liong<p/>
  <p href="#"class="justify"; </p> Motif batik ini dipengaruhi dari adanya kebudayaan China. Dimana motif yang tertuang berbentuk liong, atau bias disebut naga. Terkadang juga bermotif burung phoenix yang sedikit mirip dengan naga dalam penggambarannya. Dalam mitologi Tionghoa, motif ini menyimbolkan adanya sumber kebaikan, kesuburan, dan kemakmuran. Sehingga diharapkan ketika tertuang di dalam batik, dan batik itu dijual dan digunakan. Akan mendatangkan kemakmuran yang melimpah ruah.<br/>
    <p>&#10061;Motif Jlamprang<p/>
  <p href="#"class="justify"; </p>Motif satu ini telah dipengaruhi oleh Islam. Karena adanya dari perajin batik yang mempunyai keturunan arab dan beragama Islam. Karena di dalam agama Islam dilarang untuk menggambar makhluk hidup seperti manusia dan hewan, maka ini mendorong perajin batik dari Pekalongan untuk menciptakan motif hias geometris. Motif ini termasuk motif nitik. Batik ini mengedepankan satu pola ceplokan didalam bentuk bunga padma dan lung-lungan yang dapat menunjukan adanya makna tentang dunia kosmis yang telah datang sejak agama Budha dan Hindu di tanah Jawa.<br/>
    <p>&#10061;Motif Sawat<p/>
  <p href="#"class="justify"; </p>Maksud dari motif ini adalah melempar. Beberapa orang Jawa percaya bahwa segala kekuatan para dewa dapat mengendalikan alam semesta. Salah satu dewa yang mempunyaii senjata thathit atau kilat yaitu Batara Indra. Senjata ini digunakan dengan cara dilemparkan. Senjata ini agak menyerupai seekor ular yang mempunyai taring tajam. Batik yang menggunakan motif ini benar-benar berkembang pesat di pasaran. Karena makna yang terkandung juga berarti dapat melindungi, karena ini adalah simbol senjata dewa.<br/>
   <p>&#10061;Motif Semen<p/>
  <p href="#"class="justify"; </p>
Motif semen ini sebenarnya berupa pola yang bergambar tentang daratan dengan tumbuh-tumbuhan dan juga hewan-hewan yang ada. Sebenarnya nama motif ini berasal dari nama Ramawijaya. Dan juga terdapat 8 nasihat.<br/>
   
